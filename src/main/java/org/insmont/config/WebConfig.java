package org.insmont.config;

import org.springframework.context.annotation.Configuration;
import org.springframework.web.servlet.config.annotation.CorsRegistry;
import org.springframework.web.servlet.config.annotation.WebMvcConfigurer;

/**
 * @author chuhelan
 * @version 1.0
 * @date Saturday 17 February 2024 10:44 AM
 * @package: org.insmont.config
 * @Desc:
 */
@Configuration
public class WebConfig implements WebMvcConfigurer {
    @Override
    public void addCorsMappings(CorsRegistry registry) {
        registry.addMapping("/**")
                .allowedOrigins("https://insmont.org.cn", "http://localhost:5500",
                        "http://127.0.0.1:5500", "https://api.insmont.org.cn",
                        "https://image.chuhelan.com/", "https://insmont.org")
                .allowedMethods("GET", "POST", "PUT", "DELETE", "OPTIONS", "PATCH")
                .allowedHeaders("*")
                .allowCredentials(true)
                .maxAge(3600);
    }
}